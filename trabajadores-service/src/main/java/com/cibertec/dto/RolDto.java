package com.cibertec.dto;

public class RolDto {

	private int id_rol;
	private String roles;

	public RolDto() {
		super();
	}

	public Integer getId_rol() {
		return id_rol;
	}

	public void setId_rol(Integer id_rol) {
		this.id_rol = id_rol;
	}

	public String getRoles() {
		return roles;
	}

	public void setRoles(String roles) {
		this.roles = roles;
	}
}
